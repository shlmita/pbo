package latihan;

public class WhileLoop {
    public static void main(String[] args) {
        //jika di while tidak perlu memakai index statement dan for statement
        //while loop pengecekan di awal
        //while hanya memerlukan kondisi saja (nilai <= 10)
        var nilai = 1;

        while (nilai <= 10) {
            System.out.println("ape " + nilai);
            nilai++;
        }
    }
}
